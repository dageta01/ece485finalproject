void Hex32ToBCD(uint32_t Hex, uint8_t *Dec)
  {
  uint32_t index = 9;
	Dec[10]=0;
	while(Hex>=10)
	{
		Dec[index--] = Hex%10 + '0';
		Hex /= 10;
	}	
	Dec[index]=Hex+'0';
}

